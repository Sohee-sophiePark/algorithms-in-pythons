#%%
from typing import List

#%%
class ListNode:
    def __init__(self, val):
        self.val = val
        self.next = None

#%%
headNode = ListNode(1)
headNode.next = ListNode(2) # next - None -> next node
headNode.next.next = ListNode(3)
headNode.next.next.next = ListNode(4)

# %%
# two ways to print out value of Nodes
# 1. Iterative 2. Recursive
# 1. iterative: printNodesItr
def printNodesIter(node: ListNode):
    current_node = node
    while current_node is not None:
        print(current_node.val, end=' ')
        current_node = current_node.next

#%%
# check output1
printNodesIter(headNode)


# %%
# 2. Recursive way: printNodesRecur
def printNodesRecur(node: ListNode):
    print(node.val, end=' ')
    if node.next is not None:
        printNodesRecur(node.next)


# %%
# check output2
printNodesRecur(headNode)

